x = 6
y = 2

print(f"{x} mas {y} es {x + y}")
print(f"{x} menos {y} es {x - y}")
print(f"{x} por {y} es {x * y}")
print(f"{x} dividido {y} es {x / y}")
print(f"{x} dividido {y} es {x // y} (división entera)")
print(f"{x} módulo {y} es {x % y}")

z = 7

print(f"{z} dividido {y} es {z // y}")
print(f"El resto de dividir {z} entre {y} es {z % y}")

print(f"{x} elevado a {y} es {x ** y}")
print(f"{x} elevado a {3} es {x ** 3}")

print(f"La raíz cuadrada de {x} es {x ** 0.5}")
print(f"La raíz cuadrada de {x} es {x ** (1/2)}")